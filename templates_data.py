def get_hub_by_slug(slug):
    """Get hub data by slug"""
    return CATEGORY_HUBS.get(slug)

def get_all_hubs():
    """Get all category hubs"""
    return list(CATEGORY_HUBS.values())

def get_template_by_slug(slug):
    for template in TEMPLATES:
        if template['slug'] == slug:
            seo_content = TEMPLATE_SEO_CONTENT.get(slug, {})
            return {**template, **seo_content}
    return None

def get_templates_by_category(category):
    if category == 'All':
        return TEMPLATES
    return [t for t in TEMPLATES if t['category'] == category]
